    def save(self):
        if self.recOpen():
            if not self.regNo.isReadOnly():
                # fetching all data from form
                reg = self.regNo.text()
                nm = self.name.text()
                roll= self.roll.text()
                no = self.mob.text()
                sub=self.course.text()
                add=self.address.text()
                fee,atnd=[],[]
                for f in self.feeInps:fee.append(f.text())
                for a in self.atndInps:atnd.append(a.text())
                rmrk=self.remarkPT.toPlainText()
                # applying validations
                for name in nm.split():
                    if not name.isalpha():
                        show_msg('Enter valid name.')
                        break
                if not reg.isdigit():show_msg('Enter valid Registration No.')
                elif not roll.isdigit():show_msg('Enter valid Roll No.')
                elif not no.isdigit():show_msg('Enter valid Mobile No.')
                elif len(no) != 10:show_msg('Enter valid length Mobile No.')
                elif not sub.isalpha():show_msg('Enter valid Course.')
                elif not add.isalpha():show_msg('Enter valid Address.')
                elif not fee[0] in ('Paid','Unpaid'):show_msg('Enter valid Fee status for Sem 1')
                elif not fee[1] in ('Paid','Unpaid'):show_msg('Enter valid Fee status for Sem 2')
                elif not fee[2] in ('Paid','Unpaid'):show_msg('Enter valid Fee status for Sem 3')
                elif not fee[3] in ('Paid','Unpaid'):show_msg('Enter valid Fee status for Sem 4')
                elif not fee[4] in ('Paid','Unpaid'):show_msg('Enter valid Fee status for Sem 5')
                elif not fee[5] in ('Paid','Unpaid'):show_msg('Enter valid Fee status for Sem 6')
                elif not atnd[0].isdigit():show_msg('Enter valid Attendance for Sem 1')
                elif not atnd[1].isdigit():show_msg('Enter valid Attendance for Sem 2')
                elif not atnd[2].isdigit():show_msg('Enter valid Attendance for Sem 3')
                elif not atnd[3].isdigit():show_msg('Enter valid Attendance for Sem 4')
                elif not atnd[4].isdigit():show_msg('Enter valid Attendance for Sem 5')
                elif not atnd[5].isdigit():show_msg('Enter valid Attendance for Sem 6')
                else:
                    db = SMSDB()  # connnecting DB
                    if db.insertStud(reg,nm,roll,no,sub,add,fee,atnd,rmrk):show_msg('Student Record created.')
                    else:
                        if confirm_msg('User already exist.\nWant to Update Record'):
                            db.updateStud(reg,nm,roll,no,sub,add,fee,atnd,rmrk)
                            show_msg('Record Updated.')
                    db.conn_close()  # disconnecting DB
                    self.recClose()
                    self.lineReadOnly(True)
            else:pass
        else:show_msg('No Record to Save.')
