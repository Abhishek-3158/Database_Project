    def search(self):
        self.load.show()
        self.movie.start()
        self.lineReadOnly(True)
        srchId = self.regNoInp.toPlainText()
        if not srchId == '' and srchId.isdigit():
            db = SMSDB()
            records=[]
            if db.stud_exist(srchId):
                for table in self.tables:records.append(db.fetchStud(table,srchId))
                for item,line in zip(records[0],self.prsnlInps):line.setText(item)
                rec=records[1]
                rec=rec[1:]
                for item,line in zip(rec,self.feeInps):line.setText(item)
                rec=records[2]
                rec=rec[1:]
                for item,line in zip(rec,self.atndInps):line.setText(item)
                self.remarkPT.setPlainText(records[3][1])
            else:show_msg('User not Found.')
            db.conn_close()
        else:show_msg('Enter valid input.')
        self.movie.stop()
        self.load.hide()
